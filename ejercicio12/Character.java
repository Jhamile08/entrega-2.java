package ejercicio12;

public abstract class Character {
    private String name;
    private int level;

    public Character() {
    }

    public Character(String name, int level) {
        this.name = name;
        this.level = level;
    }

    public String getName() {
        return name;
    }

    public int getLevel() {
        return level;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public abstract void attack();

}
